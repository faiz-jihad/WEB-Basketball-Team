// Simple selector helper that works in components
export const getTranslation = (lang: Language, section: string, key: string): string => {
  try {
    const translationSection = (translations[lang] as any)[section];
    if (translationSection) {
      return translationSection[key] || (translations['en'] as any)[section][key] || key;
    }
    return key;
  } catch (e) {
    return key;
  }
};
